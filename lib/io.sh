#!/bin/sh

######################################################################
#<
#
# Function:
#      = p6_echo()
#
#
#
#>
######################################################################
p6_echo() {

    echo "$@"
}

######################################################################
#<
#
# Function:
#      = p6_msg()
#
#
#
#>
######################################################################
p6_msg() {
    p6_echo "$@"
}

######################################################################
#<
#
# Function:
#      = p6_error()
#
#
#
#>
######################################################################
p6_error() {
    p6_msg "$@" >2
}

######################################################################
#<
#
# Function:
#      = p6_die(code)
#
# Arg(s):
#    code - 
#
#
#>
######################################################################
p6_die() {
    local code="$1"
    shift

    p6_msg "$@"
    exit $code
}

######################################################################
#<
#
# Function:
#      = p6__header(indent)
#
# Arg(s):
#    indent - 
#
#
#>
######################################################################
p6__header() {
    local indent="$1"
    shift

    local h=""
    local i=0
    while [ $i -lt $indent ]; do
	h="${h}="
	i=$(($i+1))
    done

    p6_msg "$h> $@"
}

######################################################################
#<
#
# Function:
#      = p6_h1()
#
#
#
#>
######################################################################
p6_h1() {
    p6__header  "2" "$@"
}

######################################################################
#<
#
# Function:
#      = p6_h2()
#
#
#
#>
######################################################################
p6_h2() {
    p6__header  "4" "$@"
}

######################################################################
#<
#
# Function:
#      = p6_h3()
#
#
#
#>
######################################################################
p6_h3() {
    p6__header  "6" "$@"
}

######################################################################
#<
#
# Function:
#     int = p6_h4()
#
#
# Return(s):
#     -     -     int = p6_return_int(int)
#
#>
######################################################################
p6_h4() {
    p6__header  "8" "$@"
}

######################################################################
#<
#
# Function:
#     int = p6_h5()
#
#
# Return(s):
#     -     int = p6_return_int(int)
#
#>
######################################################################
p6_h5() {
    p6__header "10" "$@"
}
