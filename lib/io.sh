######################################################################
#<
#
# Function:
#     int = p6_return_int(int)
#
# Arg(s):
#    int - 
#
# Return(s):
#     - 
#
#>
######################################################################
p6_return_int() {
    local int="$1"

    return $int
}

######################################################################
