# Function: code rc = p6_math_le(a, b)
#
#  Args:
#	a - 
#	b - 
#
#  Returns:
#	code - rc
#
#>
######################################################################
p6_math_le() {
    local a="$1"
    local b="$2"

    test $a -lt $b
    local rc=$?

    p6_return_code_as_code "$rc"
}

