#!/bin/sh

######################################################################
#<
#
# Function:
#      = p6_env__debug()
#
#
#
#>
######################################################################
p6_env__debug() {
    local msg="$1"

    p6_debug "p6_env: $msg"
}

######################################################################
#<
#
# Function:
#      = p6_env_export(var, val)
#
# Arg(s):
#    var - 
#    val - 
#
#
#>
######################################################################
p6_env_export() {
    local var="$1"
    local val="$2"

    ## XXX: p6_run_eval
    eval "export $var=\"$val\""

    p6_env__debug "export(): [$var] => [$val]"
}

######################################################################
#<
#
# Function:
#      = p6_env_export_un(var)
#
# Arg(s):
#    var - 
#
#
#>
######################################################################
