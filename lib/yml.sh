# shellcheck shell=bash

######################################################################
#<
#
# Function: p6_yml__debug()
#
#>
######################################################################
p6_yml__debug() {
    local msg="$1"

    p6_debug "p6_yml: $msg"

    p6_return_void
}

######################################################################
#<
#
# Function: p6_yml_eval(yml, ...)
#
#  Args:
#	yml -
#	... - 
#
#>
######################################################################
p6_yml_eval() {
    local yml="$1"
    shift 1

    p6_echo "$yml" | yq "$@"
}

######################################################################
#<
#
# Function: p6_yml_from_file(file)
#
#  Args:
#	file -
#
#>
######################################################################
p6_yml_from_file() {
    local file="$1"

    jq <"$file"

    p6_return_void
}
