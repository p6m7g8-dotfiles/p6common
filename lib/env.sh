######################################################################
#<
#
# Function:
#      = p6_env_list(glob)
#
# Arg(s):
#    glob - 
#
#
#>
######################################################################
p6_env_list() {
    local glob="$1"

    if p6_string_blank "$glob"; then
	env
    else
	env | grep $glob
    fi
}