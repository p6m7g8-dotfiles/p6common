#!/bin/sh

######################################################################
#<
#
# Function:
#	p6_echo()
#
#>
######################################################################
p6_echo() {

    echo "$@"
}

######################################################################
#<
#
# Function:
#	p6_msg()
#
#>
######################################################################
p6_msg() {
    p6_echo "$@"
}

######################################################################
#<
#
# Function:
#	p6_error()
#
#>
######################################################################
p6_error() {
    p6_msg "$@" >2
}

######################################################################
#<
#
# Function:
#	p6_die(code)
#
#  Args:
#	code - 
#
#>
######################################################################
p6_die() {
    local code="$1"
    shift

    p6_msg "$@"
    exit $code

    p6_return_void
}

######################################################################
#<
#
# Function:
#	p6__header(indent)
#
#  Args:
#	indent - 
#
#>
######################################################################
p6__header() {
    local indent="$1"
    shift

    local h=""
    local i=0
    while [ $i -lt $indent ]; do
	h="${h}="
	i=$(($i+1))
    done

    p6_msg "$h> $@"

    p6_return_void
}

######################################################################
#<
#
# Function:
#	p6_h1()
#
#>
######################################################################
p6_h1() {

    p6__header  "2" "$@"
    p6_return_void
}

######################################################################
#<
#
# Function:
#	p6_h2()
#
#>
######################################################################
