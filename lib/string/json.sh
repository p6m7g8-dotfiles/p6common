# shellcheck shell=bash

######################################################################
#<
#
# Function: p6_json__debug()
#
#>
######################################################################
p6_json__debug() {
    local msg="$1"

    p6_debug "p6_json: $msg"

    p6_return_void
}

######################################################################
#<
#
# Function: p6_json_eval(json, ...)
#
#  Args:
#	json -
#	... - 
#
#>
######################################################################
p6_json_eval() {
    local json="$1"
    shift 1

    p6_echo "$json" | jq "$@"
}

######################################################################
#<
#
# Function: p6_json_from_file(file)
#
#  Args:
#	file -
#
#>
######################################################################
p6_json_from_file() {
    local file="$1"

    jq <"$file"

    p6_return_void
}
