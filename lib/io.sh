p6_return_bool() {
    local bool="$1"

    return $bool
}

######################################################################
#<
#
# Function:
#     str = p6_return_true()
#
#
# Return(s):
#     - 
#     - 
#
#>
######################################################################
p6_return_true() {

    p6_return_bool 1
}

######################################################################
#<
#
# Function:
