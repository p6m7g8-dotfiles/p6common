######################################################################
p6_return() {
    local rv="$1"

    p6_echo "$rv"

    p6_return_code 0
}

######################################################################
#<
#
# Function:
#	p6_return_code(rc, 
#
#  Args:
#	rc - 
#
#>
######################################################################
p6_return_code() {
    local rc="$1"

    return $rc
}

